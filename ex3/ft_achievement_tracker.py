#!/usr/bin/env python3


def main():
    print("=== Achievement Tracker System ===\n")

    alice = {
        "first_kill",
        "level_10",
        "treasure_hunter",
        "speed_demon"
    }

    bob = {
        "first_kill",
        "level_10",
        "boss_slayer",
        "collector"
    }

    charlie = {
        "level_10",
        "treasure_hunter",
        "boss_slayer",
        "speed_demon",
        "perfectionist"
    }

    print(f"Player alice achievements: {alice}")
    print(f"Player bob achievements: {bob}")
    print(f"Player charlie achievements: {charlie}\n")

    print("=== Achievement Analytics ===")

    all_achievements = alice.union(bob, charlie)
    print(f"All unique achievements: {all_achievements}")
    print(f"Total unique achievements: {len(all_achievements)}\n")

    common_all = alice.intersection(bob, charlie)
    print(f"Common to all players: {common_all}")

    rare = all_achievements.difference(
        alice.intersection(bob),
        alice.intersection(charlie),
        bob.intersection(charlie)
    )
    print(f"Rare achievements (1 player): {rare}\n")

    print(f"Alice vs Bob common: {alice.intersection(bob)}")
    print(f"Alice unique: {alice.difference(bob)}")
    print(f"Bob unique: {bob.difference(alice)}")


if __name__ == "__main__":
    main()
